export function getFusionResult(arcanaA: string, arcanaB: string): string | null {
  const normalizedA = ARCANA_ALIASES[arcanaA] || arcanaA;
  const normalizedB = ARCANA_ALIASES[arcanaB] || arcanaB;
  
  const indexA = ARCANA_INDEX[normalizedA as Arcana];
  const indexB = ARCANA_INDEX[normalizedB as Arcana];
  
  if (indexA === undefined || indexB === undefined) {
    return null;
  }
  
  if (normalizedA === normalizedB) {
    const result = FUSION_MATRIX[indexA]?.[indexB];
    if (result === '—') return null;
    return ARCANA_ALIASES[result] || result;
  }
  
  const result = FUSION_MATRIX[indexA]?.[indexB];
  if (result === '—') return null;
  return ARCANA_ALIASES[result] || result;
}

export function getFusionResults(arcanaA: string, arcanaB: string): string[] {
  const results: string[] = [];
  
  const result1 = getFusionResult(arcanaA, arcanaB);
  if (result1) results.push(result1);
  
  const result2 = getFusionResult(arcanaB, arcanaA);
  if (result2 && result2 !== result1) results.push(result2);
  
  return results.length > 0 ? results : [''];
}

