export function getFusionResult(arcanaA: string, arcanaB: string): string | null {
  const normA = ARCANA_ALIASES[arcanaA] || arcanaA;
  const normB = ARCANA_ALIASES[arcanaB] || arcanaB;
  
  if (normA === normB) return normA;
  
  const res = (table[normA]?.[normB]) || (table[normB]?.[normA]);
  return res ? (ARCANA_ALIASES[res] || res) : null;
}

export function getFusionResults(arcanaA: string, arcanaB: string): string[] {
  const res = getFusionResult(arcanaA, arcanaB);
  return res ? [res] : [''];
}
